def field_name_matches_ttt_pattern(field_name: str) -> bool:
    """Check if a field name matches any known TTT field pattern.

    Case-insensitive. Strips dotted path prefixes (e.g. "field_3.emotional_register"
    → check "emotional_register").

    Args:
        field_name: The field name or dotted path to check.

    Returns:
        True if the last segment of the field name matches a TTT pattern.
    """
    last_segment = field_name.lower().split(".")[-1].strip()
    return last_segment in TTT_FIELD_PATTERNS


# ─── Phase 2: Implicit Value String Patterns ─────────────────────────────────

# Spec §Detection Algorithm Phase 2:
# "Scan all Block B field VALUES for TTT scale references"
# IMPORTANT: Implicit register instructions WITHOUT explicit TTT labels/values
# are NOT caught by C-08 (by design — see Spec §TTT Contamination Vectors note).

_RAW_TTT_VALUE_PATTERNS: list[str] = [
    r"TTT-\d{1,2}",                          # "TTT-04", "TTT-10", "TTT-7"
    r"ttt[-_]\d{1,2}",                       # "ttt-04", "ttt_07" (case-insensitive flag needed)
    r"temperature\s*[:=]\s*\d",              # "temperature: 7", "temperature=4"
    r"tone\s*[:=]\s*\w",                     # "tone: confrontational"
    r"register\s*[:=]\s*\w",                 # "register: warm"
    r"write\s+at\s+TTT",                     # "write at TTT-07"
    r"maintain\s+.*TTT",                     # "maintain TTT-04 throughout"
    r"emotional_heat\s*[:=]\s*\d",           # "emotional_heat: 6"
    r"heat_level\s*[:=]\s*\d",              # "heat_level: 5"
    r"voice_intensity\s*[:=]\s*\d",          # "voice_intensity: 8"
    r"temperature\s*[=:]\s*ttt",             # "temperature = ttt-07"
    r"static.*temperature.*\d",              # "static temperature value: 5"
    r"override_ttt\s*[:=]",                  # "override_ttt: TTT-05" (adapter inline)
]

TTT_VALUE_PATTERNS: list[re.Pattern[str]] = [
    re.compile(pattern, re.IGNORECASE) for pattern in _RAW_TTT_VALUE_PATTERNS
]


def value_contains_ttt_pattern(value_string: str) -> tuple[bool, str | None]:
    """Check if a string value contains an implicit TTT reference.

    Args:
        value_string: The field value string to scan.

    Returns:
        Tuple of (matched: bool, matched_pattern_str: str | None).
    """
    for pattern in TTT_VALUE_PATTERNS:
        match = pattern.search(value_string)
        if match:
            return True, pattern.pattern
    return False, None


