def get_affinity_range(archetype_id: str) -> TTTAffinityRange | None:
    """Look up the natural affinity range for a given archetype.

    Args:
        archetype_id: The content archetype identifier.

    Returns:
        TTTAffinityRange if found, None if the archetype is not registered.
    """
    return AFFINITY_RANGE_MAP.get(archetype_id)


def is_temperature_within_affinity(archetype_id: str, temperature: int) -> bool:
    """Check if a temperature is within the archetype's natural affinity range.

    Returns True if within range OR if the archetype is not registered.
    This is advisory only — never a gate.

    Args:
        archetype_id: Content archetype identifier.
        temperature: Coach's authenticated temperature (1-10).

    Returns:
        True if within affinity range (or archetype unknown). False if outside.
    """
    affinity = get_affinity_range(archetype_id)
    if affinity is None:
        return True  # Unknown archetypes are not gated
    return affinity.min_temperature <= temperature <= affinity.max_temperature


