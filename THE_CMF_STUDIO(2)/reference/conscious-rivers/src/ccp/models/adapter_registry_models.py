"""
CCP Step 5 — Adapter Registry Models (Unit 1)
Shared Pydantic v2 models for all JIT Skill Compiler Adapter Registry services.

Architecture reference:
    CCP_Technical_Architecture.md §4 Adapter Registry v2.0
    JIT Skill Compiler — Block A (Voice DNA / Trigger pre-load) and
                         Block B (Psychological / Constraint injection)

Step 5 builds 4 of the 5 mandatory adapters:
    Adapter-1: coach-soul-adapter        (DEP-ENG-003 → Block A)
    Adapter-2: negative-space-loader-adapter  (DEP-ENG-004 → Block A FIRST)
    Adapter-4: psych-routing-adapter     (DEP-LIB-001 + DEP-ENG-016 → Block B)
    Adapter-5: irevc-adapter             (DEP-LIB-002 + DEP-ENG-005 → Block A)

Shared invariants:
    ADR-01: all payloads carry coach_id for single-tenant isolation
    FR47:   all adapters write to ReceiptChain using DEP-ENG-041 schema
    M-02:   no TTT hardcoded values in any adapter output — DEP-ENG-005 only
"""

from __future__ import annotations

from enum import Enum
from typing import Any, Optional

from pydantic import BaseModel, Field


# ─── Adapter Registry Slot IDs ────────────────────────────────────────────────

class AdapterSlot(str, Enum):
    """Official registry slot identifiers from CCP_Technical_Architecture.md §4.

    Mandatory adapters (Step 5 scope):
        COACH_SOUL               Adapter-1
        NEGATIVE_SPACE_LOADER    Adapter-2
        PSYCH_ROUTING            Adapter-4
        IREVC                    Adapter-5

    Built elsewhere:
        CONTEXT_PREMISE          Adapter-3   (FR13 — Step 13)
        PAYLOAD_MASKING          Adapter-6   (FR22 — Step 7)
        AUDIENCE_MATURITY        Adapter-7   (FR20 — Step 4, BUILT)
        CRAL_FINDING_ROUTER      Adapter-8   (Step 11)
    """
    COACH_SOUL = "coach-soul-adapter"
    NEGATIVE_SPACE_LOADER = "negative-space-loader-adapter"
    CONTEXT_PREMISE = "context-premise-adapter"
    PSYCH_ROUTING = "psych-routing-adapter"
    IREVC = "irevc-adapter"
    PAYLOAD_MASKING = "payload-masking-adapter"
    AUDIENCE_MATURITY = "audience-maturity-adapter"
    CRAL_FINDING_ROUTER = "cral-finding-router-adapter"


# ─── Block Injection Targets ──────────────────────────────────────────────────

class BlockTarget(str, Enum):
    """SKILL.md block targets for adapter injection.

    Block A: Structural laws — Voice DNA, Trigger pre-load, stylometry prose
    Block B: Pre-generation constraints — Psychological routing, maturity, masking
    Block C: Compilation gates — Not injected by adapters (authoring-time only)
    """
    BLOCK_A = "Block_A"
    BLOCK_B = "Block_B"


# ─── Rhythm Instruction ───────────────────────────────────────────────────────

class RhythmInstruction(BaseModel):
    """A single rhythm / pacing instruction generated by the psych-routing-adapter.

    FR8 §C-08 exception: Sentence Rhythm instructions produced by the
    Psychological Routing Adapter are classified as Structural Mechanics.
    Gate C-08 MUST whitelist these — they are NOT TTT value injections.

    bypass_c08 = True must be present on ALL rhythm instructions from
    the psych-routing-adapter so the C-08 scanner can skip them correctly.
    """
    instruction_text: str = Field(
        description="Literal rhythm/pacing instruction string for SKILL.md."
    )
    bypass_c08: bool = Field(
        default=True,
        description=(
            "FR8 C-08 exception flag. True = this instruction is a Structural Mechanic "
            "generated by the Psychological Routing Adapter — C-08 MUST whitelist it. "
            "Never set False on adapter-generated rhythm instructions."
        ),
    )
    source_variable: Optional[str] = Field(
        default=None,
        description="Which DEP-LIB-001 variable drove this instruction (e.g. 'V3', 'V2').",
    )


# ─── Block A Injection ────────────────────────────────────────────────────────

class BlockAInjection(BaseModel):
    """Structured output for all Block A (structural law) adapter injections.

    Block A carries Voice DNA prose, Trigger pre-load, and Negative Space
    contrastive strings that govern the generation agent's stylistic behaviour.

    ADR-01: coach_id must be present for all non-test injections.
    """
    adapter_slot: AdapterSlot = Field(
        description="Which adapter produced this injection."
    )
    coach_id: str = Field(
        description="ADR-01 tenant isolation — coach instance identifier."
    )
    target: BlockTarget = Field(
        default=BlockTarget.BLOCK_A,
        description="Always Block A for this model.",
    )
    section_header: str = Field(
        description="Section header for the SKILL.md Block A section (e.g. '## Negative Space Laws')."
    )
    structural_laws: list[str] = Field(
        default_factory=list,
        description="Ordered list of structural law strings for Block A injection.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Traceability metadata: dep_id, gate_results, source_counts.",
    )

    def to_block_a_text(self) -> str:
        """Render this injection as a SKILL.md Block A section string."""
        lines = [self.section_header, ""]
        for law in self.structural_laws:
            lines.append(f"- {law}")
        lines.append("")
        return "\n".join(lines)


# ─── Block B Injection ────────────────────────────────────────────────────────

class BlockBInjection(BaseModel):
    """Structured output for all Block B (constraint) adapter injections.

    Block B carries psychological routing variables, depth constraints,
    masking instructions, and maturity gates for the generation agent.

    M-02 hard rule: NO TTT values (TTT-NN) may appear in any constraint string.
    TTT is resolved at runtime via DEP-ENG-005 only (FR8 Layer 3).

    ADR-01: coach_id must be present for all non-test injections.
    """
    adapter_slot: AdapterSlot = Field(
        description="Which adapter produced this injection."
    )
    coach_id: str = Field(
        description="ADR-01 tenant isolation — coach instance identifier."
    )
    target: BlockTarget = Field(
        default=BlockTarget.BLOCK_B,
        description="Always Block B for this model.",
    )
    section_header: str = Field(
        description="Section header for the SKILL.md Block B section."
    )
    constraint_strings: list[str] = Field(
        default_factory=list,
        description="Ordered list of CONSTRAINT strings for Block B injection.",
    )
    rhythm_instructions: list[RhythmInstruction] = Field(
        default_factory=list,
        description=(
            "Rhythm/pacing instructions from psych-routing-adapter. "
            "All carry bypass_c08=True — FR8 C-08 whitelist mandate."
        ),
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Traceability metadata: dep_id, source variable resolution.",
    )

    def to_block_b_text(self) -> str:
        """Render this injection as a SKILL.md Block B section string."""
        lines = [self.section_header, ""]
        for c in self.constraint_strings:
            lines.append(f"- {c}")
        if self.rhythm_instructions:
            lines.append("")
            lines.append("### Rhythm Instructions (Structural Mechanics — C-08 Whitelisted)")
            for r in self.rhythm_instructions:
                lines.append(f"- [RHYTHM|bypass_c08={r.bypass_c08}] {r.instruction_text}")
        lines.append("")
        return "\n".join(lines)


# ─── Adapter Run Result ───────────────────────────────────────────────────────

class AdapterRunResult(BaseModel):
    """Top-level result from any single adapter invocation.

    Carries the injection payload (Block A or B), receipt ID for audit,
    and any gate failures that halted the adapter.
    """
    adapter_slot: AdapterSlot = Field(
        description="Which adapter produced this result."
    )
    coach_id: str = Field(
        description="ADR-01 tenant isolation."
    )
    success: bool = Field(
        description="True if adapter ran without gate failures."
    )
    block_a: Optional[BlockAInjection] = Field(
        default=None,
        description="Block A injection payload (if this adapter targets Block A).",
    )
    block_b: Optional[BlockBInjection] = Field(
        default=None,
        description="Block B injection payload (if this adapter targets Block B).",
    )
    receipt_id: str = Field(
        default="",
        description="FR47 receipt ID from the ReceiptChain.log() call.",
    )
    gate_failures: list[str] = Field(
        default_factory=list,
        description="List of gate failure messages if success=False.",
    )
    warnings: list[str] = Field(
        default_factory=list,
        description="Non-blocking advisory messages (e.g. TTT outside affinity range).",
    )


# ─── Voice DNA Adapter Pipeline Result ───────────────────────────────────────

class VoiceDNAAdapterPipelineResult(BaseModel):
    """Composite result from the full Step 5 adapter pipeline run.

    Collects all 4 adapter results and assembles the complete
    Block A + Block B injection payload for SKILL.md compilation.

    Load order (Mandate 4 enforced by pipeline orchestrator):
        1. negative-space-loader-adapter (DEP-ENG-004) FIRST
        2. coach-soul-adapter            (DEP-ENG-003)
        3. irevc-adapter                 (DEP-LIB-002 + DEP-ENG-005)
        4. psych-routing-adapter         (DEP-LIB-001 + DEP-ENG-016)
    """
    coach_id: str = Field(
        description="ADR-01 tenant isolation."
    )
    pipeline_receipt_id: str = Field(
        default="",
        description="Top-level orchestration receipt ID.",
    )
    negative_space_result: Optional[AdapterRunResult] = Field(
        default=None,
        description="Result from Adapter-2: negative-space-loader-adapter.",
    )
    coach_soul_result: Optional[AdapterRunResult] = Field(
        default=None,
        description="Result from Adapter-1: coach-soul-adapter.",
    )
    irevc_result: Optional[AdapterRunResult] = Field(
        default=None,
        description="Result from Adapter-5: irevc-adapter.",
    )
    psych_routing_result: Optional[AdapterRunResult] = Field(
        default=None,
        description="Result from Adapter-4: psych-routing-adapter.",
    )
    all_success: bool = Field(
        default=False,
        description="True if all 4 adapters completed without gate failures.",
    )
    mandate_4_enforced: bool = Field(
        default=False,
        description=(
            "True when negative-space-loader completed before coach-soul-adapter. "
            "Mandate 4 — hardcoded pipeline enforcement, not a prompt instruction."
        ),
    )

    def get_all_block_a_injections(self) -> list[BlockAInjection]:
        """Return all Block A injections in Mandate-4-compliant load order."""
        results = []
        for result in [
            self.negative_space_result,
            self.coach_soul_result,
            self.irevc_result,
        ]:
            if result and result.success and result.block_a:
                results.append(result.block_a)
        return results

    def get_all_block_b_injections(self) -> list[BlockBInjection]:
        """Return all Block B injections."""
        results = []
        if self.psych_routing_result and self.psych_routing_result.success and self.psych_routing_result.block_b:
            results.append(self.psych_routing_result.block_b)
        return results
